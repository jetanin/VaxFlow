N_HOSPITALS = 100   # จำนวนโรงพยาบาลที่สร้าง (วนใช้ 77 จังหวัด)


def _hospital(i):
    """กำหนดประเภท/ขนาด/พิกัด รพ. ลำดับที่ i (วนจังหวัด + กระจายพิกัดเมื่อมีหลายสาขา)."""
    rng = np.random.default_rng(1000 + i)
    province, lat, lon = PROVINCES[i % len(PROVINCES)]
    frac = i / N_HOSPITALS
    if frac < 0.13:
        prefix, scale = "รพศ.", round(rng.uniform(1.1, 1.4), 2)   # โรงพยาบาลศูนย์
    elif frac < 0.40:
        prefix, scale = "รพท.", round(rng.uniform(0.8, 1.1), 2)   # โรงพยาบาลทั่วไป
    elif frac < 0.73:
        prefix, scale = "รพช.", round(rng.uniform(0.5, 0.8), 2)   # โรงพยาบาลชุมชน
    else:
        prefix, scale = "รพ.สต.", round(rng.uniform(0.3, 0.5), 2) # รพ.ส่งเสริมสุขภาพตำบล
    branch = i // len(PROVINCES)                                  # รอบที่วนซ้ำจังหวัด
    name = f"{prefix} {province}" + (f" สาขา {branch + 1}" if branch else "")
    # กระจายพิกัดเล็กน้อยเมื่อมีหลายสาขาในจังหวัดเดียว (ไม่ให้หมุดทับกัน)
    jit = (lambda v: round(v + rng.uniform(-0.15, 0.15), 4)) if branch else (lambda v: round(v, 4))
    return (f"HOSP_{i + 1:03d}", name, scale, jit(lat), jit(lon), int(rng.integers(1, 5)))


# สร้างโรงพยาบาล (id, ชื่อ, ขนาด-scale, พิกัด, lead time)
HOSPITALS = [_hospital(i) for i in range(N_HOSPITALS)]


def main():
    master_rows = [
        {"hospital_id": hid, "name": name,
         "latitude": lat, "longitude": lon, "lead_time_days": lead}
        for hid, name, _scale, lat, lon, lead in HOSPITALS
    ]
    master = pd.DataFrame(master_rows)
    master.to_csv(OUT / "hospital_master.csv", index=False, encoding="utf-8-sig")
    print(f"[saved] hospital_master.csv: {len(master)} hospitals")


if __name__ == "__main__":
    main()
